# Chương trình đổi kg sang g
#
# 1 kg = 1000 g
#
# Công thức:
# g = kg * 1000
#
# Ví dụ:
# Nếu kg = 2 thì:
# g = 2 * 1000 = 2000
#
# Ý tưởng:
# - Nhập số kilogram.
# - Nhân số kilogram với 1000.
# - In kết quả ra màn hình.

kg = float(input("Nhập số kilogram: "))

g = kg * 1000

print(kg, "kg =", g, "g")
