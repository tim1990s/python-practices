# Chương trình đổi km sang m
#
# 1 km = 1000 m
#
# Công thức:
# m = km * 1000
#
# Ví dụ:
# Nếu km = 2.5 thì:
# m = 2.5 * 1000 = 2500
#
# Ý tưởng:
# - Nhập số kilomet.
# - Nhân số kilomet với 1000.
# - In kết quả ra màn hình.

km = float(input("Nhập số kilomet: "))

m = km * 1000

print(km, "km =", m, "m")
