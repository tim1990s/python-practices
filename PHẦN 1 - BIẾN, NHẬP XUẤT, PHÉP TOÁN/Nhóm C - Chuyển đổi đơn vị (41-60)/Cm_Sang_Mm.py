# Chương trình đổi cm sang mm
#
# 1 cm = 10 mm
#
# Công thức:
# mm = cm * 10
#
# Ví dụ:
# Nếu cm = 12 thì:
# mm = 12 * 10 = 120
#
# Ý tưởng:
# - Nhập số centimet.
# - Nhân số centimet với 10.
# - In kết quả ra màn hình.

cm = float(input("Nhập số centimet: "))

mm = cm * 10

print(cm, "cm =", mm, "mm")
