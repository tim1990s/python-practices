# Chương trình tính lợi nhuận
#
# Lợi nhuận là phần tiền còn lại sau khi lấy doanh thu trừ chi phí.
#
# Công thức:
# loi_nhuan = doanh_thu - chi_phi
#
# Ý tưởng:
# - Nhập doanh thu.
# - Nhập chi phí.
# - Lấy doanh thu trừ chi phí.

doanh_thu = float(input("Nhập doanh thu: "))
chi_phi = float(input("Nhập chi phí: "))

loi_nhuan = doanh_thu - chi_phi

print("Lợi nhuận là:", loi_nhuan)
