# Chương trình nhập 2 số và tính tổng
#
# Tổng của hai số là kết quả nhận được khi cộng hai số đó lại với nhau.
#
# Công thức:
# tong = a + b
#
# Ví dụ:
# Nếu a = 5 và b = 3 thì:
# Tổng = 5 + 3 = 8
#
# Ý tưởng:
# - Nhập số thứ nhất là a.
# - Nhập số thứ hai là b.
# - Cộng a và b để tính tổng.
# - In kết quả ra màn hình.

a = float(input("Nhập số a: "))
b = float(input("Nhập số b: "))

tong = a + b

print("Tổng của", a, "và", b, "là:", tong)
