# Chương trình tính bình phương của một số
#
# Bình phương của một số là kết quả khi nhân số đó với chính nó.
#
# Công thức:
# binh_phuong = n * n
#
# Ví dụ:
# Nếu n = 5 thì:
# Bình phương = 5 * 5 = 25
#
# Ý tưởng:
# - Nhập số n.
# - Nhân n với n để tính bình phương.
# - In kết quả ra màn hình.

n = float(input("Nhập số n: "))

binh_phuong = n * n

print("Bình phương của", n, "là:", binh_phuong)
