# Chương trình tính tích 3 số
#
# Tích của 3 số là kết quả nhận được khi nhân cả 3 số với nhau.
#
# Công thức:
# tich = a * b * c
#
# Ví dụ:
# Nếu a = 2, b = 3, c = 4 thì:
# Tích = 2 * 3 * 4 = 24
#
# Ý tưởng:
# - Nhập ba số a, b và c.
# - Nhân ba số để tính tích.
# - In kết quả ra màn hình.

a = float(input("Nhập số a: "))
b = float(input("Nhập số b: "))
c = float(input("Nhập số c: "))

tich = a * b * c

print("Tích của 3 số là:", tich)
