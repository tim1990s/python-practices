# Chương trình nhập 2 số và tính tích
#
# Tích của hai số là kết quả nhận được khi nhân hai số đó với nhau.
#
# Công thức:
# tich = a * b
#
# Ví dụ:
# Nếu a = 6 và b = 4 thì:
# Tích = 6 * 4 = 24
#
# Ý tưởng:
# - Nhập số thứ nhất là a.
# - Nhập số thứ hai là b.
# - Nhân a với b để tính tích.
# - In kết quả ra màn hình.

a = float(input("Nhập số a: "))
b = float(input("Nhập số b: "))

tich = a * b

print("Tích của", a, "và", b, "là:", tich)
