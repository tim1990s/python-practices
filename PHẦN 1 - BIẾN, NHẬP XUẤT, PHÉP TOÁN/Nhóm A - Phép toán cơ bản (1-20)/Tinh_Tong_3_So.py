# Chương trình tính tổng 3 số
#
# Tổng của 3 số là kết quả nhận được khi cộng cả 3 số lại với nhau.
#
# Công thức:
# tong = a + b + c
#
# Ví dụ:
# Nếu a = 2, b = 3, c = 4 thì:
# Tổng = 2 + 3 + 4 = 9
#
# Ý tưởng:
# - Nhập ba số a, b và c.
# - Cộng ba số để tính tổng.
# - In kết quả ra màn hình.

a = float(input("Nhập số a: "))
b = float(input("Nhập số b: "))
c = float(input("Nhập số c: "))

tong = a + b + c

print("Tổng của 3 số là:", tong)
